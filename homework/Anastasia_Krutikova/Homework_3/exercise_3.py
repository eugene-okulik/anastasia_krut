# Даны два числа. Найти среднее арифметическое и среднее геометрическое этих чисел

number_1 = 10
number_2 = 12

mean = (10 + 12) / 2
print("Среднее арифметическое чисел number_1 и number_2 =", mean)

geom_mean = (number_1 * number_2) ** 0.5
print("Среднее геометрическое чисел number_1 и number_2 =", geom_mean)
